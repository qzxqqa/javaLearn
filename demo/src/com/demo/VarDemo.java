public class VarDemo {

	public static void main(String[] args) {
	    int m = 10;
	    int n = 5;
	   
	    //变量m的值加3，n的值加5
	    m += 3;
	    n += 5;
	 
	    //求m和n的平均值，并将结果存于变量p中
	    int p = (int)((m + n)/2);
	    
	    //求m的平方乘以n的平方，并将结果存于变量q中
	    int q = m * m*n *n;

	    //将p和q的值打印输出
	    System.out.println("p="+p);
	    System.out.println("p="+p);
	}

}
